    public boolean isExistAdmin(){
        for (int i = 0; i < GymManager1.getAdmins().size(); i++) {
                if(jTextField1.getText().trim().equalsIgnoreCase(GymManager1.getAdmins().get(i).getUsername()) && jPasswordField1.getText().trim().equalsIgnoreCase(GymManager1.getAdmins().get(i).getPassword())){
                   
                    return true;
                }
        }
        return false;
        
    }
    private void jLabel5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel5MouseClicked
        
        
        if(jTextField1.getText().trim().equalsIgnoreCase("") || jPasswordField1.getText().trim().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(null, "Please Enter All Fields");
        }
        else{
            if(isExistAdmin()){
                this.setVisible(false);
                new Home1().setVisible(true);
            }
            else{
                
                JOptionPane.showMessageDialog(null, "Incorrect entry!");
            }
          
        }
        
        
        
    }//GEN-LAST:event_jLabel5MouseClicked

